namespace feng3d
{

    /**
     * 加载数据类型
     */
    export enum LoaderDataFormat
    {
        /**
         * 以原始二进制数据形式接收下载的数据。
         */
        BINARY = 'binary',
        /**
         * 以文本形式接收已下载的数据。
         */
        TEXT = 'text',
        /**
         * 图片数据
         */
        IMAGE = 'image',
    }
}